public class TipeDataNumber {
    // main function
    public static void main(String[] args) {
        /**
         * Tipe Data Number adalah tipe data yang ada di dalam bahasa pemrograman.
         * Pada tipe data number ini memiliki beberapa jenis tipe data dimana yaitu ada
         * Integer Number (Bilangan bulat), Literal Number (Bilangan Hexa, dll).
         * dan Floating, Double number
         */

        // Integer Number
        byte iniByte = 10;
        short iniShort = 100;
        int iniInt = 1000;
        long iniLong = 10000;
        long iniLong2L = 1000000L;

        // Floating, Doube number
        double iniDouble = 10.10;
        float iniFloating = 10.10F;

        // Literals Number
        int hexa = 0x4E2;
        int decimal = 8;
        int octalNumber = 01247;
        int Binarynumber = 0b010101;

        // underscore number
        int harga = 10_000_000;

        // contoh memanggil hasil literal number hexa
        System.out.println(hexa);
    }
}
