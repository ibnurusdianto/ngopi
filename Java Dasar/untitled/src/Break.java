public class Break {
    // main function
    public static void main(String[] args) {
        /**
         * Break adalah terminate atau kondisi berhenti
         * Berikut contohnya
         */
        for (int x = 0; x < 30; x++){
            if (x == 5){
                break;
            }
            System.out.println(x);
        }
    }
}
