public class Variable {
    // main function
    public static void main(String[] args) {
        String nama;
        int umur;
        double nilai;

        nama = "Ibnu Rusdianto";
        umur = 22;
        nilai = 3.70;

        // bisa juga menggunakan kata kunci var
        // dimana kata kunci ini tidak perlu tipe data numbernya

        /**
         * Kata kunci var adalah kata kunci yang wajib harus di-deklarasikan
         */
        var alamat = "Subang";

        /**
         * Bisa  juga final, dimana final ini tidak bisa diubah , berikut contohnya
         */

        final String nama2 = "Heru"; // maka variable nama2 tidak bisa diubah.
    }
}
