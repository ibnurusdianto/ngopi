public class OperasiPerbandingan {
    // main function
    public static void main(String[] args) {
        int nilai = 20;
        int nilai2 = 25;

        System.out.println(nilai > nilai2);
        System.out.println(nilai >= nilai2);
        System.out.println(nilai < nilai2);
        System.out.println(nilai <= nilai2);
        System.out.println(nilai != nilai2);
        System.out.println(nilai == nilai2);
    }
}
