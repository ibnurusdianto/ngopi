public class TipeDataStrings {
    // main function
    public static void main(String[] args) {
        String namaLengkap, alamat;
        namaLengkap = "Ibnu Rusdianto";
        alamat = "Subang, Cigugur Kidul";
        int umur = 22;

        System.out.println("Halo Nama saya adalah : " + namaLengkap);
        System.out.println("Umur saya adalah : " + umur);

        /**
         * Dalam pembuatan string juga kita bisa menggabungkan string tersebut
         */

        String namaDepan = "Ibnu";
        String namaBelakang = "Rusdianto";
        System.out.println("Nama asli adalah : " + namaDepan + " " + namaBelakang);

    }
}
