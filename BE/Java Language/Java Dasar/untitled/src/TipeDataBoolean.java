public class TipeDataBoolean {
    // main function
    public static void main(String[] args) {
        /**
         * Tipe Data boolean adalah tipe data kebenaran dimana hanya memiliki 2 nilai
         * yaitu true dan false, dimana true adalah benar
         * dan false adalah salah
         */

        boolean benar = true;
        boolean salah = false;

        System.out.println("Boolean benar adalah : " + benar);
        System.out.println("Boolean salah adalah : " + salah);
    }
}

