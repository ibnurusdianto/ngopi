public class StringClasess {
    public static void main(String[] args) {
        String nama = "Ibnu Rusdianto";

        // konversi ke string lowecase
        String lowecase = nama.toLowerCase();
        // konversi ke String Uppercase
        String uppercase = nama.toUpperCase();

        System.out.println(nama);
        System.out.println(lowecase);
        System.out.println(uppercase);

        // dan masih banyak lainnya

    }
}
